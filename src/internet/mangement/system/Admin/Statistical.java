    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        String customerName = txtSearchCustomer.getText().trim();
        Date startDate = dtStartDate.getDate();
        Date endDate = dtEndDate.getDate();

        // Validate date range if both dates are provided
        if ((startDate != null && endDate == null) || (startDate == null && endDate != null)) {
            JOptionPane.showMessageDialog(this,
                "Vui lòng chọn cả ngày bắt đầu và ngày kết thúc",
                "Lỗi",
                JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (startDate != null && endDate != null && startDate.after(endDate)) {
            JOptionPane.showMessageDialog(this,
                "Ngày bắt đầu không thể sau ngày kết thúc",
                "Lỗi",
                JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Apply filters
        getConditionRecords(customerName, startDate, endDate);
    }//GEN-LAST:event_btnViewActionPerformed

