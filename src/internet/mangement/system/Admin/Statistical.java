    public final void getConditionRecords(String customerName, Date startDate, Date endDate) {
        String[] columnNames = {"Mã thuê bao", "Tên khách hàng", "Gói cước", "Ngày đăng ký", "Trạng thái", "Số tiền đã thanh toán"};
        NonEditableTableModel model = new NonEditableTableModel(null, columnNames);
        tblStatistical.setModel(model);

        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd-MM-yyyy HH:mm:ss");

        ArrayList<StatisticalRecord> records = StatisticalDAO.getByCondition(customerName, startDate, endDate);

        if (records != null && !records.isEmpty()) {
            for (StatisticalRecord record : records) {
                String formattedDate = record.getRegistration_date() != null ?
                        record.getRegistration_date().format(formatter) : "";

                model.addRow(new Object[]{
                    record.getSubscriber_id(),
                    record.getCustomer_name(),
                    record.getPlan_name(),
                    formattedDate,
                    record.getStatus(),
                    ControllerUtils.formatMoney(record.getAmount_paid())
                });
            }
        }

        setupTableSelectionListener();
    }

